#define width 100 
#define height 72
#define n_threads 4



typedef struct{
 int stride;
 int start;

}argument;
  
argument arguments[n_threads];

unsigned char image[], results[width * height];

void threadFunction()
 {
  int i;
 pthread_t t[n_threads];	
 void *detect_edges();	
  for (i=0;i<n_threads;i++)
 {
 
  arguments[i].start = i;
  arguments[i].stride = n_threads;

 }

  for(i=0; i<n_threads;i++){
  void *detect_edges();
  pthread_create(&t[i],NULL,detect_edges,&arguments[i]);

  pthread_join(t[i],NULL);
  }

 }

void *detect_edges(argument *args) {
  int i;
  int n_pixels = width * height;


  for(i=args->start;i<n_pixels;i+=args->stride) {
    int x, y; // the pixel of interest
    int b, d, f, h; // the pixels adjacent to x,y used for the calculation
    int r; // the result of calculate
    
    y = i / width;
    x = i - (width * y);

    if (x == 0 || y == 0 || x == width - 1 || y == height - 1) {
      results[i] = 0;
    } else {
      b = i + width;
      d = i - 1;
      f = i + 1;
      h = i - width;

      r = (image[i] * 4) + (image[b] * -1) + (image[d] * -1) + (image[f] * -1)
          + (image[h] * -1);

      if (r > 0) { // if the result is positive this is an edge pixel
        results[i] = 255;
      } else {
        results[i] = 0;
      }
    }
  }
}

